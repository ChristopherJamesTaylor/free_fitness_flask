<h3 class="dpg-h3">Flexible Dieting Versus Old-School Dieting</h3>
<p>
If you've been through the wringer of old-school dieting, it can be hard to see how IIFYM is any different than a free-for-all, but being in a caloric deficit is still crucial to losing fat, no matter where your foods are coming from. Let's compare the two methods.
</p>

<h4 class="dpg-h4">Difference 1 <span class="extra-condensed-regular">Restrictive behaviors</span></h4>
<p>
Many old-school diet approaches are based around strict calorie counts, approved food lists, and sometimes complete elimination of a food or food group to lose weight (think low-fat or low-carb). This approach may work initially, but it often doesn't last.
</p>
<p>
When the dieter is feeling deprived and low-energy, a cheat meal turns into a <a href="https://www.bodybuilding.com/fun/ask-the-nutrition-tactician-whats-the-difference-between-a-refeed-and-a-cheat-day" target="_blank">cheat day</a>, which turns into an eventual lack of care for the original diet. Within weeks, they end up back at their pre-diet weight, if not heavier. This is the vicious yo-yo dieting cycle.
</p>

<img class="dpg-full-center" src="https://www.bodybuilding.com/images/2018/march/your-complete-guide-to-iifym-reskin-2.jpg" alt="IIFYM">
<div class="dpg-pullquote">A flexible approach avoids the mentality that certain foods are "off limits" and promotes the inclusion of a variety of foods.</div>

<p>
IIFYM avoids the mentality that certain foods are "off limits" and promotes moderation and inclusion of a variety of foods. Rather than worrying about breaking the bank, you can indulge regularly with a bit of sweet or savory and still work toward your goal without a sense of guilt.
</p>
<p>
It's all about portion control. Dieting will still be tough, and you might still feel hungry at times, but taking a moderation-focused approach will make it a lot more bearable.
</p>

<h4 class="dpg-h4">Difference 2 <span class="extra-condensed-regular">Athletic performance</span></h4>
<p>
Anyone who has ever tried to train hard while on a strict caloric-restriction planâ€”or one that's extremely low-carb or low-fatâ€”knows how hard that can be. Your workouts suffer dramatically, as does your overall energy level.
</p>
<p>
Sure, abs may be ultimately "made in the kitchen," as they say, but they're also made through month upon month of high-quality training. If you want to improve your body composition in a lasting way, this is non-negotiable! Extreme caloric restriction won't get you there.
</p>
<p>
IIFYM places a focus on the importance of each macronutrient, respecting the unique role that each plays. Those who have success with it find that it enables them to feel more energized and train hard even while losing weight.
</p>

<h4 class="dpg-h4">Difference 3 <span class="extra-condensed-regular">Social-situation success</span></h4>
<p>
If you've ever been on a diet that had a listâ€”whether implied or stated outrightâ€”of "forbidden" foods, you know how even the thought of cake can make you feel guilty. Far too often, people indulge and then punish themselves later on by eating, well, <i>nothing</i>, or very close to it. This isn't a healthy or sustainable approach.
</p>
<p>
Following an IIFYM approach promotes less stress in these sorts of situations. You can just eat more or less of a certain macro than planned at an earlier meal. But don't forget you still have to meet your macros! It's a highly flexible system that is meant to coincide with your lifestyle and daily choicesâ€”as long as you're keeping track.
</p>

<img class="dpg-full-center" src="https://www.bodybuilding.com/images/2016/april/your-complete-guide-to-iifym-3.jpg">
<div class="dpg-pullquote">With a flexible approach to nutrition, you can have your cake and eat it, too, whether at home, a restaurant, or social gathering.</div>

<p>
This transitions nicely to <a href="https://www.bodybuilding.com/fun/4-tips-for-sticking-to-your-diet-when-dining-out" target="_blank">dining out</a>, too. Restaurants often provoke anxiety in dieters due to their lack of control of portions and cooking methods. Of course, many restaurants will never be totally transparent about calories or macros, but once you've got the hang of the macro game, some back-of-the-napkin math should be enough to help you find options.
</p>
<p>
One upside of the global obesity problem is that more restaurants than ever have their nutrition available online, and some even have it on hand. Checking the menu ahead of time to determine what may fit, or what you will be <i>spending</i>, will allow you to enjoy your meal without worrying about getting off track.
</p>

<h4 class="dpg-h4">Difference 4 <span class="extra-condensed-regular">The research backs up IIFYM</span></h4>
<p>
Multiple studies over the years have shown how ineffective the old-school restriction-based approach to dieting can be, and the stats back it up. But is IIFYM any better? It turns out that there has been research comparing old-school dieting to flexible dieting.
</p>
<p>
A study comparing the two found that people following a restrictive approach to dieting were more likely to have a higher BMI, reduced feelings of self-control, and more psychological stress related to weight and food intake.<sup>6</sup> Chalk this up as another victory for IIFYM.
</p>

<h3 class="dpg-h3">Avoiding Common Pitfalls of IIFYM</h3>
<p>
I'm not going to defend all versions of IIFYM. Plenty of people do it wrong, and they leave ample Instagram evidence of every meal along the way. This has given the approach a reputation as being unhealthy, even though it shouldn't be! I've already discussed some of these problems in my "<a href="https://www.bodybuilding.com/fun/ask-the-nutrition-tactician-how-can-i-make-iifym-as-healthy-as-possible" target="_blank">Ask the Nutrition Tactician</a>" column.
</p>

<h4 class="dpg-h4">Pitfall 1 <span class="extra-condensed-regular">Missing micros</span></h4>
<p>
Vitamins and minerals, also known as micronutrients, are crucial for having a body that functions as well as it looks. They play crucial roles in your training and recovery, too! Unfortunately, many IIFYM adherents opt for cookies over carrots too frequently.
</p>

<img class="dpg-full-center" src="https://www.bodybuilding.com/images/2018/march/your-complete-guide-to-iifym-reskin-3.jpg" alt="IIFYM">
<div class="dpg-pullquote">For optimal health and performance, don't use IIFYM as an excuse to skip veggies in favor of cookies.</div>

<p>
<strong>Fix it:</strong> Make the conscious decision to incorporate a variety of nutrient-dense foods such as whole grains, <a href="https://www.bodybuilding.com/fun/the-most-nutritious-fruits-and-vegetables.html" target="_blank">fruits, and vegetables</a>. They should be the norm, not the exception. In fact, I argue that you should make one serving of nonstarchy vegetables both "free" and mandatory at least three times a day.
</p>

<h4 class="dpg-h4">Pitfall 2 <span class="extra-condensed-regular">Suboptimal protein</span></h4>
<p>
Sure, fatty steaks and the occasional fried food can be worked into your day, but this comes at a serious caloric cost. Additionally, if you're unintelligently sifting through plant-based proteins, you may be missing out on key essential amino acids (EAAs) necessary to promote optimal muscle growth and recovery.
</p>
<p>
<strong>Fix it:</strong> Know the difference between a complete and incomplete protein; low-quality proteins shouldn't be counted towards your total protein score. I like to use the example of peanut butter. It may have 8 grams of protein per serving, but it's an incomplete protein, meaning it's lacking in one or more of the essential amino acids. Unless you put it on some toast, or pair it with a complete protein (any protein source coming from an animal) you should be considering this only as a fat. Save your protein numbers for low-fat, high-quality sources. These are nutrient-dense and will support growth, recovery, and satiety without providing excess calories.
</p>
<p>
If you're following the <a href="https://www.bodybuilding.com/fun/12-complete-vegetarian-proteins.html" target="_blank">vegetarian route</a>, be sure that you're pairing complementary proteins, such as rice and beans, or choosing complete, plant-based proteins, such as quinoa and soy, to take in ample EAAs during the day.
</p>

<img class="dpg-full-center" src="https://www.bodybuilding.com/images/2018/march/your-complete-guide-to-iifym-reskin-4.jpg" alt="IIFYM">
<div class="dpg-pullquote">Vegetarian meals should include complete proteins such as quinoa or tofu, or pair two complementary proteins.</div>


<h4 class="dpg-h4">Pitfall 3 <span class="extra-condensed-regular">Catastrophic carbohydrate choices</span></h4>
<p>
Similar to skimping on micros, many favor sweet sugary goodness over wholesome, fiber-rich deliciousness. I'm talking about a Snicker's bar over oats, or Skittles over an apple. They may both be carbs, but the difference is one you can feel: a short spike in energy and a subsequent crash soon after. This makes it both difficult to train effectively and to be productive throughout the day.
</p>

<img class="dpg-full-center" src="https://www.bodybuilding.com/images/2016/april/your-complete-guide-to-iifym-5b.jpg">
<div class="dpg-pullquote">The result of choosing low-fiber carbohydrate options is a short spike in energy and a subsequent crash soon after.</div>

<p>
<strong>Fix it:</strong> Don't think IIFYM is license to eat like a child. Yes, you can certainly work white bread, chips, cookies, and candies in on occasion, but they're still treatsâ€”not staples. Plan your meals out whenever possible, and design them around high-fiber carbohydrate options such as oats, brown rice, and other foods that provide a sustained release of energy. Not all carbs are equal! The source, <a href="https://www.bodybuilding.com/fun/time-your-carbs-perfectly-to-crush-your-goals" target="_blank">timing</a>, and amount all have big implications on your energy, performance, and recovery.
</p>

<h4 class="dpg-h4">Pitfall 4 <span class="extra-condensed-regular">Lack of essential fats</span></h4>
<p>
Pizza, doughnuts, and ice cream sure do taste phenomenal, but if they're your only source of fat intake, we have a problem. These foods are high in certain fats, sure, but they're essentially empty of the healthy fats that can improve heart health, cholesterol levels, and help to promote a leaner body composition.<sup>7-9</sup>
</p>
<p>
<strong>Fix it:</strong> Keep your treats as treats. The rest of the time, stick to things like nuts, seeds, egg yolks, oils, and avocados to provide you with a plentiful blend of heart-healthy, recovery-supporting fats.
</p>

<h3 class="dpg-h3">IIFYM for the Experienced Dieter</h3>
<p>
The longer you've been training and tracking your food, the more you know about how your body responds to certain levels of fat or carbs. However, if you want to dial in your macros to match your training and food preference, I recommend nailing down your protein and then working on finding the balance between the other two macros. Here's how that process looks:
</p>
<ul>
    <li><strong>Step 1:</strong> Find your estimated daily calorie needs to meet your goal.</li>
	<li><strong>Step 2:</strong> Aim to consume 1 gram of protein per pound of body weight (or your amount of choice).</li>
	<li><strong>Step 3:</strong> Fill the rest of your calories with carbohydrates and fat as you see fit. Some prefer a higher carbohydrate approach, while others prefer a high-fat, moderate-carbohydrate approach.</li>
</ul>

