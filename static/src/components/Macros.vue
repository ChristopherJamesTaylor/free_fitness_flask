<h3 class="dpg-h3">If It Fits Your Macros 101</h3>
<p>
Macros, short for "macronutrients," is the term used to describe the three major (or macro) nutrients: <a href="https://www.bodybuilding.com/content/what-are-the-benefits-of-dietary-protein.html">protein</a>, carbohydrates, and fat. Rather than placing all of your attention on counting calories or demonizing certain macronutrients, IIFYM focuses on meeting daily macro goals. Done correctly, this provides a consistent calorie intake, but one that can be personalized in endless different ways.
</p>
<p>
At first glance this may seem needlessly complicated: "Why count three different things when I can count just one?" The answer is that by emphasizing specific amounts of each macronutrient (and choosing quality sources), you can better tailor it to your lifestyle, tastes, and goals.
</p>

<img class="dpg-full-center" src="https://www.bodybuilding.com/images/2018/march/your-complete-guide-to-iifym-reskin-1.jpg" alt="IIFYM">
<div class="dpg-pullquote">By focusing on macronutrients, you can tailor your nutrition plan to your lifestyle, tastes, and goals.</div>

<p>
Imagine 2,000 calories. Hard to do, right? You could get to that number countless different ways, both unhealthy and healthy. One version of a 2,000-calorie day could help you fuel fantastic workouts, add muscle, and feel full. Another could leave you on the same-old blood sugar roller coaster, feeling cloudy and dragging butt through your training.
</p>
<p>
In other words, you need another layer of structure! Managing macros is similar to managing your monetary budget. Rather than being able to spend whatever you want as long as it hits a grand caloric total, you have to budget three different currenciesâ€”protein, carbohydrates, and fat. If you want to spend your carbs on a big pancake breakfast, you certainly can, but remember that you only have so much to spend and that you still need to hit your protein and fat totals for the day. And don't be surprised if you're hurting later that afternoon because you've spent all of your carbohydrates and are now lacking energy to power through the rest of the day.
</p>
<p>
However, the beauty of IIFYM is that you can do this very thing every once in a while and still stay on track as long as you're on point the remainder of the day. So, if you have a special occasion planned for later in the evening, you can adjust your intake throughout the day to ensure you have plenty to spend at dinner.
</p>

<h3 class="dpg-h3">Meet Your Macros</h3>
